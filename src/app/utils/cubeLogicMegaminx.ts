export function generateScrambleMegaminx(length = 70) {
  const moves = [];
  for (let i = 0; i < length / 10; i++) {
    for (let j = 0; j < 5; j++) {
      moves.push(Math.random() < 0.5 ? 'R++' : 'R--');
      moves.push(Math.random() < 0.5 ? 'D++' : 'D--');
    }
    moves.push(Math.random() < 0.5 ? 'U' : "U'");
  }
  return moves.join(' ');
}

export function applyMoveMegaminx(state, move) {
  const oldState = state.megaminx;
  const newState = new Array(132);
  let P;
  switch (move) {
    case 'U': P = permU; break;
    case "U'": P = permUPrime; break;
    case 'R++': P = permRPlus; break;
    case 'R--': P = permRMinus; break;
    case 'D++': P = permDPlus; break;
    case 'D--': P = permDMinus; break;
    default: return state;
  }
  for (let i = 0; i < 132; i++) newState[i] = oldState[P[i]];
  return { megaminx: newState };
}

export function applyScrambleMegaminx(scramble) {
  let state = createSolvedMegaminx();
  if (!scramble) return state;
  const moves = scramble.split(' ').filter(m => m.trim());
  for (const move of moves) {
    state = applyMoveMegaminx(state, move);
  }
  return state;
}
